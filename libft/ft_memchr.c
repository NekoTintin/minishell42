#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			index;
	unsigned char	*ptr;

	index = 0;
	ptr = (unsigned char *)s;
	while (index < n)
	{
		if (ptr[index] == (unsigned char)c)
			return ((void *)&ptr[index]);
		index++;
	}
	return (NULL);
}
